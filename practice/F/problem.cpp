int g(int i, int j) {
    int ans = 0;
    for (int k = 0; k < 100; ++k)
        if (abs(i+j-k)<abs(ans-i-j))
            ans = k;
    return ans;
}

void f(int n) {
    vii am(n, vi(n));
    for (int i = 0; i < n; ++i)
        for (int j = 0; j < n; ++j) {
            am[i][j] = (1 + i + j + i * i + j * j) % n;
            am[i][j] += 10;
            if (i == j)
                am[i][j] = 0;
        }
    for (int k = 0; k < n; ++k)
        for (int i = 0; i < n; ++i)
            for (int j = 0; j < n; ++j)
                am[i][j] = min(am[i][j], g(am[i][k] ,am[k][j]));
    debug(am[0][n-1]);
}

